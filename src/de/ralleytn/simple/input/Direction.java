package de.ralleytn.simple.input;

/**
 * Represents a direction.
 * @author Ralph Niemitz/RalleYTN(ralph.niemitz@gmx.de)
 * @version 1.0.0
 * @since 1.0.0
 */
public enum Direction {

	/**
	 * @since 1.0.0
	 */
	NORTH,
	
	/**
	 * @since 1.0.0
	 */
	NORTH_EAST,
	
	/**
	 * @since 1.0.0
	 */
	EAST,
	
	/**
	 * @since 1.0.0
	 */
	SOUTH_EAST,
	
	/**
	 * @since 1.0.0
	 */
	SOUTH,
	
	/**
	 * @since 1.0.0
	 */
	SOUTH_WEST,
	
	/**
	 * @since 1.0.0
	 */
	WEST,
	
	/**
	 * @since 1.0.0
	 */
	NORTH_WEST;
}
